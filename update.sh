#!/bin/bash
# ══════════════════════════════════════════════
#  ЧИТАЛКА — обновить код на GitHub
#  Запускай каждый раз после изменений
# ══════════════════════════════════════════════

set -e
cd "$(dirname "$0")"

MSG="${1:-Обновление читалки}"

git add -A
git commit -m "$MSG" --allow-empty
git push

echo "✅ Обновлено: $MSG"
echo "   Изменения появятся через ~30 секунд"
